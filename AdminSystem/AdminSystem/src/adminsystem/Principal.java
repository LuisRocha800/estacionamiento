        char[] password = txtPss.getPassword();
        
        String usuario = txtUsr.getText(); 
        String passwrd = new String(password);
        
        String url = ConexionEstatica.CONEXION_CREDENTIALS;
		
			try {
				
	            conexion = DriverManager.getConnection(url);
        		
             	
	            adminusersDAODerbyImp admusr = new adminusersDAODerbyImp();
	            admusr.setConexion(conexion);
	            adminusers usradm = new adminusers();
	            usradm.setUsuario(usuario);
	            
	            usradm = admusr.get(usradm);
                  
                    if(usradm.getUsuario() == null){
                      
                        JOptionPane.showMessageDialog(null, "NO EXISTE ESE USUARIO, INTENTA DE NUEVO");
                    }else if(usradm.getUsuario().equals(usuario) && usradm.getPassword().equals(passwrd) ){
                       
                       JOptionPane.showMessageDialog(null, "HOLA BIENVENIDO "+ usuario);
                       home hm = new home();
                       hm.setVisible(true);
                       this.setVisible(false);
                    }else{
                        
                       JOptionPane.showMessageDialog(null, "CREDENCIALES INCORRECTAS, TRY IT");
                    }
         
				}catch(SQLException ex) {
				ex.printStackTrace();
			}
